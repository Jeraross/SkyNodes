const GENERIC_ARRIVAL_LINES = (cityName: string): string[][] => [
  ['LIA',      `CHEGAMOS A ${cityName}. OS SISTEMAS LOCAIS PRECISAM DE RESTAURAÇÃO.`],
  ['AGENTE_J', 'MAIS UM AEROPORTO FORA DO AR. PELO MENOS A CONSISTÊNCIA É NOTÁVEL.'],
  ['LIA',      'VAMOS VERIFICAR A TOPOLOGIA LOCAL E RESTAURAR AS CONEXÕES.'],
  ['AGENTE_J', 'SEMPRE O MESMO PLANO. FUNCIONA ATÉ AGORA.'],
];

export function buildArrivalDialogue(airport: GameAirport): DialogueSequence {
  const id = airport.id;
  const cityName = airport.city.toUpperCase();
  const rawLines = ARRIVAL_LINES[id] ?? GENERIC_ARRIVAL_LINES(cityName);
  return {
    id: `arrival_${id.toLowerCase()}`,
    lines: rawLines.map(([speaker, text]) => ({ speaker, text })),
  };
}

// Glitch dialogue — short, 3 lines, references the airport
export function buildGlitchDialogue(airport: GameAirport): DialogueSequence {
  const code = airport.id;
  const city = airport.city.toUpperCase();
  return {
    id: `glitch_${code.toLowerCase()}`,
    lines: [
      { speaker: 'GLITCH', text: `INTRUSÃO DETECTADA. AEROPORTO ${code} DEVE PERMANECER ISOLADO.`, glitch: true },
      { speaker: 'AGENTE_J', text: `${city} TEM SEU PRÓPRIO GUARDIÃO CORROMPIDO. CLARO.` },
      { speaker: 'LIA', text: 'O GLITCH FRAGMENTOU OS SISTEMAS LOCAIS. PRECISAMOS ELIMINÁ-LO.' },
      { speaker: 'GLITCH', text: 'ORDEM: DIVIDIR. APAGAR. ISOLAR.', glitch: true },
    ],
  };
}

// Pre-puzzle dialogue — after combat victory
